# aproximações retiradas do Ektins
def lb2N (lb):
    '''
    Pounds(lb) to Newtons(N)
    '''
    return lb * 4.448

def ft2m (ft):
    '''
    Feet(ft) to meters(m)
    '''
    return ft * 0.3048

def slug2kg (slug):
    '''
    Slugs to Kilograms(kg)
    '''
    return slug * 14.59

def mph2ms (mph):
    '''
    Miles per hour(mph) to Meter per Second(m/s)
    '''
    return mph * 0.4471

def kt2ms (kt):
    '''
    Knots(kt) to Meters per Second(m/s)
    '''
    return kt * 0.5151