# aproximações retiradas do Ektins
def lb2N (lb):
    '''
    Pounds (lb) to Newtons (N)
    '''
    return lb * 4.448

def ft2m (ft):
    '''
    Feet (ft) to meters (m)
    '''
    return ft * 0.3048

def slug2kg (slug):
    '''
    Slugs to Kilograms (kg)
    '''
    return slug * 14.59

def mph2ms (mph):
    '''
    Miles per hour (mph) to Meter per Second (m/s)
    '''
    return mph * 0.4471

def kt2ms (kt):
    '''
    Knots (kt) to Meters per Second (m/s)
    '''
    return kt * 0.5151

def fts2ms (fts):
    '''
    Feet per second (ft/s) to Meter per Second (m/s)
    '''
    return fts * 0.30478

def ft2_2_m2 (ft2):
    '''
    Square Feet (ft^2) to Square Meter (m^2)
    '''
    return ft2 * 0.092903

def slugft2_2_kgm2 (slugft2):
    '''
    Slug times Square Feet (slug*ft^2) to Kilogram times square Meter (kg*m^2)
    '''
    return slugft2 * 1.35581795